package com.mycompany.myapplication.dto;

public class Cacao {

    private int cNo;
    private int cPhoto;
    private String cContent;
    private int cStar;
    private String cName;

    public int getcNo() {
        return cNo;
    }

    public void setcNo(int cNo) {
        this.cNo = cNo;
    }

    public int getcPhoto() {
        return cPhoto;
    }

    public void setcPhoto(int cPhoto) {
        this.cPhoto = cPhoto;
    }

    public String getcContent() {
        return cContent;
    }

    public void setcContent(String cContent) {
        this.cContent = cContent;
    }

    public int getcStar() {
        return cStar;
    }

    public void setcStar(int cStar) {
        this.cStar = cStar;
    }

    public String getcName() {
        return cName;
    }

    public void setcName(String name) {
        this.cName = name;
    }
}
